// Bitwise AND
// Result bit is 1 only if both bits are 1
console.log(3 & 5) // 1 (convert 3 and 5 into binary like 0011 0101)

// Bitwise OR
// Result bit is 1 if atleast one of the bit is 1
console.log(3 | 5) // 7

// Bitwise XOR
// Result bit is 1 only one of them is 1
console.log(3 ^ 5) // 6

// Bitwise NOT
console.log(~51) // -52 formula -(x+1)
console.log(~5) // -6
console.log(~(-98)) // 97    -(-98+1)  =>  -(-97)  =>  97

// left shift
//shifts bits to the left
//Each shift = multiple by 2
console.log(5 << 1) // 5*2**1
console.log(5 << 2) // 5*2**2

// right shift
// shifts bits to the right
// Each shift = divide by 2
console.log(5 >> 1)
console.log(15 >> 2)





